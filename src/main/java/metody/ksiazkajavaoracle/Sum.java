package metody.ksiazkajavaoracle;

public class Sum {
    public static void main(String[] args) {
        int i;
        int sum = 0 ;
        //Sumuje liczby do 5
        for (i = 1; i <= 5; sum +=i++) {
            System.out.println(i);
        }

        System.out.println("Suma wynosi " +sum +" " +i);

    }

}
