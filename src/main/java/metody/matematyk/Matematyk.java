package metody.matematyk;

public class Matematyk {

    public double add(double skladnikA, double sklanidkB) {
        double result = skladnikA + sklanidkB;
        return result;
    }

    public double subtraction (double skladnikA, double sklanidkB) {
        double result = skladnikA - sklanidkB;
        return result;

    }

    public double multiplication (double skladnikA, double skladnikB){
        double result = skladnikA*skladnikB;
        return result;

    }
    public double division (double skladnikA, double skladnikB){
        double result = skladnikA/skladnikB;
        return result;
    }

    public double areasquare (double skladnikA,double skladnikB){
        double result = skladnikA*skladnikB;
        return result;
    }
    public double areatriangle (double skladnikA, double skladnikB){
        double result = 0.5*skladnikA*skladnikB;
        return result;    }

}
